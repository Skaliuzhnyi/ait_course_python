# Создать функцию shape(n), которая принемает целое число n
# и выводит на экран квадрат из символов '*' размером n x n.

def shape(n):
    for _ in range(0, n):
        # for _ in range(0, n):
        #     print('*', end='\t')
        print('* ' * n)  # Выводим строку из n символов '*' с пробелами между ними
        print()  # New line after each row


